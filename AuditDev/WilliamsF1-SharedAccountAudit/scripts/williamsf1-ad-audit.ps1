    New-Item -ItemType Directory -Path $OutDir -Force | Out-Null
}

# === INIT REPORT HEADERS ===

# CSV
"Username,DisplayName,OU,LastLogon,Enabled,Groups,GPOs,ACLs,Exchange,DriveMaps" | Out-File $csvOut -Encoding UTF8

# Markdown
