$htmlHeader = @"
<!DOCTYPE html>
<html lang='en'>
<head>
<meta charset='UTF-8'>
<title>Williams F1 Shared Account Audit</title>
<style>
  body        { font-family: 'Segoe UI', sans-serif; margin: 20px; background: #1e1e1e; color: #ccc; }
  h1          { color: #fff; }
  table       { width: 100%; border-collapse: collapse; margin-top: 12px; }
  th, td      { border: 1px solid #333; padding: 10px; text-align: left; }
  th          { background: #007acc; color: white; }
  tr:nth-child(even) { background-color: #2a2a2a; }
  tr:hover    { background-color: #444; }
</style>
</head>
<body>
<h1>Williams F1 – Shared Account Audit</h1>
<p><b>Generated:</b> $ts</p>
<script src="https://cdn.jsdelivr.net/npm/sortablejs@latest/Sortable.min.js"></script>
<table id="auditTable">
<thead>
<tr>
<th onclick="sortTable(0)">Username</th>
<th onclick="sortTable(1)">Display Name</th>
<th onclick="sortTable(2)">OU</th>
<th onclick="sortTable(3)">Enabled</th>
<th onclick="sortTable(4)">Last Logon</th>
<th onclick="sortTable(5)">Group Count</th>
</tr>
</thead>
<tbody>
</tbody>
</table>
<script>
function sortTable(n) {
  var table, rows, switching, i, x, y, shouldSwitch, dir = "asc", switchcount = 0;
  table = document.getElementById("auditTable");
  switching = true;
  while (switching) {
    switching = false; rows = table.rows;
    for (i = 1; i < (rows.length - 1); i++) {
      shouldSwitch = false;
      x = rows[i].getElementsByTagName("TD")[n];
      y = rows[i + 1].getElementsByTagName("TD")[n];
      if ((dir == "asc" && x.innerHTML.toLowerCase() > y.innerHTML.toLowerCase()) ||
          (dir == "desc" && x.innerHTML.toLowerCase() < y.innerHTML.toLowerCase())) {
        shouldSwitch = true; break;
      }
    }
    if (shouldSwitch) {
      rows[i].parentNode.insertBefore(rows[i + 1], rows[i]); switching = true; switchcount++;
    } else {
      if (switchcount == 0 && dir == "asc") { dir = "desc"; switching = true; }
    }
  }
}
</script>
