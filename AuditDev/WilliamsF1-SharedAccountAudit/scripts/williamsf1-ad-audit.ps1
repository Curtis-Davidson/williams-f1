$htmlHeader | Out-File $htmlOut -Encoding UTF8

# JSON init
$jsonData = @()

# === MAIN AUDIT LOOP ===
$users = Get-AllUsers
foreach ($u in $users) {
    $ouPath = Get-OUPath $u.DistinguishedName
    $groupNames = @()

    try {
        $groupDNs = $u.MemberOf
        $groupNames = $groupDNs | ForEach-Object { (Get-ADGroup $_).Name }
        $groupNames += $groupDNs | ForEach-Object { Get-NestedGroups $_ }
        $groupNames = $groupNames | Sort-Object -Unique
    } catch {}

    # Resolve GPOs + ACLs + other mappings
    $gpos    = Get-GPOsForUser $u.DistinguishedName
    $acls    = Get-ACLsForUser $u.SamAccountName
    $drives  = Get-MappedDrives $u.SamAccountName
    $exch    = Get-ExchangeBindings $u.SamAccountName

    # === WRITE TO CSV ===
    "$($u.SamAccountName),$($u.DisplayName),$ouPath,$($u.LastLogonDate),$($u.Enabled),""$($groupNames -join '; ')"" ,""$($gpos -join '; ')"" ,""$($acls -join '; ')"" ,""$($exch -join '; ')"" ,""$($drives -join '; ')""" |
            Out-File $csvOut -Append -Encoding UTF8

    # === WRITE TO MARKDOWN ===
    "| $($u.SamAccountName) | $($u.DisplayName) | $ouPath | $($u.LastLogonDate) | $($u.Enabled) | $($groupNames -join ', ') |" |
            Out-File $mdOut -Append -Encoding UTF8

    # === WRITE TO HTML ===
    "<tr><td>$($u.SamAccountName)</td><td>$($u.DisplayName)</td><td>$ouPath</td><td>$($u.LastLogonDate)</td><td>$($u.Enabled)</td><td>$($groupNames -join ', ')</td></tr>" |
            Out-File $htmlOut -Append -Encoding UTF8

    # === STRUCTURED JSON OUTPUT ===
    $jsonData += [PSCustomObject]@{
        Username   = $u.SamAccountName
        Display    = $u.DisplayName
        OU         = $ouPath
        LastLogon  = $u.LastLogonDate
        Enabled    = $u.Enabled
        Groups     = $groupNames
        GPOs       = $gpos
        ACLs       = $acls
        Drives     = $drives
        Exchange   = $exch
    }

    # === LOG ===
    Add-Content $log "`n=== [$($u.SamAccountName)] ==="
    Add-Content $log "OU: $ouPath"
    Add-Content $log "Groups:"
    $groupNames | ForEach-Object { Describe-Group $_ | Add-Content $log }
    Add-Content $log "`nGPOs:`n$($gpos -join "`n")"
    Add-Content $log "`nACLs:`n$($acls -join "`n")"
    Add-Content $log "`nExchange:`n$($exch -join "`n")"
    Add-Content $log "`nDrives:`n$($drives -join "`n")"
}

# === CLOSE HTML TABLE BODY AND DOCUMENT ===
"</tbody></table></body></html>" | Out-File $htmlOut -Append -Encoding UTF8

# === JSON EXPORT ===
$jsonData | ConvertTo-Json -Depth 5 | Out-File $jsonOut -Encoding UTF8

# === XLSX EXPORT BLOCK ===
$excelPath = "$OutDir\SharedAccounts-$ts.xlsx"

try {
    if (-not (Get-Module -ListAvailable -Name ImportExcel)) {
        Write-Host "[INFO] ImportExcel module not found — attempting install..." -ForegroundColor Yellow
        Install-Module -Name ImportExcel -Scope CurrentUser -Force
    }
    Import-Module ImportExcel -ErrorAction Stop

    $jsonData | Export-Excel -Path $excelPath -WorksheetName 'SharedAccounts' `
        -TableName 'AuditReport' -AutoSize -BoldTopRow -FreezeTopRow `
        -Title "Williams F1 Shared Account Audit – $ts"

    Write-Host "[XLSX] Export complete: $excelPath" -ForegroundColor Green
} catch {
    Write-Warning "[XLSX] Excel export failed: $_"
}

# === FINAL STATUS ===
Write-Host "`n[COMPLETE] AD AUDIT"
Write-Host " CSV     : $csvOut"
Write-Host " Markdown: $mdOut"
Write-Host " HTML    : $htmlOut"
Write-Host " JSON    : $jsonOut"
Write-Host " Log     : $log"

# === SNAPSHOT LOGIC ===

$snapDir     = "$PSScriptRoot\..\snapshots"
$snapFile    = "$snapDir\SharedAccounts-$($ts.Split('_')[0]).json"
$latestSnap  = Get-ChildItem -Path $snapDir -Filter "SharedAccounts-*.json" | Sort-Object LastWriteTime -Descending | Select-Object -First 1

# Ensure snapshot directory exists
if (!(Test-Path $snapDir)) {
    New-Item -Path $snapDir -ItemType Directory -Force | Out-Null
}

# Save current snapshot
try {
    $jsonData | ConvertTo-Json -Depth 5 | Set-Content -Path $snapFile -Encoding UTF8
    Write-Host "[SNAPSHOT] Saved baseline snapshot to: $snapFile" -ForegroundColor Green
} catch {
    Write-Warning "[SNAPSHOT] Failed to write snapshot: $_"
}

# === DRIFT DETECTION (COMPARE LAST SNAPSHOT TO THIS RUN) ===
if ($latestSnap) {
    try {
        $prev = Get-Content $latestSnap.FullName | ConvertFrom-Json
        $curr = $jsonData

        $diffUsers = Compare-Object $prev.Username $curr.Username -PassThru
        $diffText = @()

        if ($diffUsers.Count -gt 0) {
            $diffText += "`n=== Snapshot Drift Detected ==="
            $diffText += "Modified or Missing Users: $($diffUsers -join ', ')"
            Add-Content $log $diffText
            Write-Warning "[SNAPSHOT] Drift detected! Users changed since last snapshot."
        } else {
            Write-Host "[SNAPSHOT] No drift detected from previous snapshot." -ForegroundColor DarkGray
        }

    } catch {
        Write-Warning "[SNAPSHOT] Drift check failed: $_"
        Add-Content $log "`n[SNAPSHOT] Drift check error: $_"
    }
}

